package pharmacy.entities;

import custom.orm.db.utils.annotations.*;

@Entity(tableName = "med_category")
public class MedCategory {

    @Id
    @AutoGenerated
    int id;

    String name;

    public MedCategory(String name) {
        this.name = name;
    }

    public MedCategory(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public MedCategory() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
